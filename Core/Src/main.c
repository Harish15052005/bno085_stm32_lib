// Initialize the DWT cycle counter for microsecond timing
uint32_t DWT_Delay_Init(void) {
	/* Enable TRC */
	CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
	/* Reset the clock cycle counter value */
	DWT->CYCCNT = 0;
	/* Enable clock cycle counter */
	DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;

	// Quick check to make sure it started
	if (DWT->CYCCNT) {
		return 0; // Success
	} else {
		return 1; // Failed
	}
}

// The micros() function
uint32_t micros(void) {
	// DWT->CYCCNT counts CPU clock cycles.
	// Dividing by (SystemCoreClock / 1000000) converts cycles to microseconds.
	return DWT->CYCCNT / (SystemCoreClock / 1000000U);
}

// The millis() function
uint32_t millis(void) {
	// STM32 HAL already tracks milliseconds in the background!
	return HAL_GetTick();
}

